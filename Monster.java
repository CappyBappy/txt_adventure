public class Monster {
    String name;
    int health;
    int attack;
    int mAttack;
    int defense;
    int mDefense; 
    public Monster(String nam, int healt, int attacc, int mAttacc, int defen, int mDefen) {
        name=nam;
        health=healt;
        attack=attacc;
        mAttack=mAttacc;
        defense=defen;
        mDefense=mDefen;
    }
    
}
